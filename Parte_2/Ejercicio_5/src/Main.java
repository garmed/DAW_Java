public class Main {
    public static void main(String[] args) {
        System.out.println("------------------------------------------------------------");
        System.out.println("5. Muestra los números múltiplos de 5 de 0 a 100 utilizando un bucle for. ");
        System.out.println("------------------------------------------------------------");
        //variable


        //solicitud de datos


        //Algoritmo
        for (int i=0; i<=100;i++){
            if (i%5==0){
                System.out.println(i);
            }
        }

        //Resultados
        System.out.println("-----------------------------------------------------------");
    }
}